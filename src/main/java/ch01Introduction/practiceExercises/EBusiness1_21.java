package ch01Introduction.practiceExercises;

/**
 * To speak more than one language is a valuable skill in the labor market today. One of the basic skills is learning to
 * greet people. Write a program that prints a two-column list with the greeting phrases shown in the table.
 *
 * List of Phrases to Translate
 * - Good morning
 * - It is a pleasure to meet you.
 * - Please call me tomorrow.
 * - Have a nice day!
 *
 * In the first column, print the phrase in English, in the second column, print the phrase in a language of your choice.
 * If you don’t speak a language other than English, use an online translator or ask a friend.
 *
 * Difficulty
 * expected: easy
 * actual: easy
 */
public class EBusiness1_21 {

  public static void printGreetingsInSpanish() {
    System.out.println("+-------------------------------+-----------------------------+");
    System.out.println("| Good morning.                 | Buenos dias.                |");
    System.out.println("| It is a pleasure to meet you. | Es un placer en conocerla.  |");
    System.out.println("| Please call me tomorrow.      | Por favor llameme mañana.   |");
    System.out.println("| Have a nice day!              | Que tenga un buen dia!      |");
    System.out.println("+-------------------------------+-----------------------------+");
  }
}
