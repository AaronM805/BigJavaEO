package ch01Introduction.practiceExercises;

/**
 * Write a program that prints an imitation of a Piet Mondrian painting. (Search the
 * Internet if you are not familiar with his paintings.) Use character sequences such as
 * @@@ or ::: to indicate different colors, and use - and | to form lines.
 *
 * Difficulty
 * expected: medium
 * actual: easy
 */
public class E1_9 {
  public void printMondrianPainting() {
    System.out.println("+--------------------------------------------+");
    System.out.println("|    ::@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@|");
    System.out.println("|    ::@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@|");
    System.out.println("|    ::@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@|");
    System.out.println("|::::::@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@|");
    System.out.println("|::::::@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@|");
    System.out.println("|    ::@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@|");
    System.out.println("|    ::@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@|");
    System.out.println("|    ::@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@|");
    System.out.println("|    ::@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@|");
    System.out.println("|::::::::::::::::::::::::::::::::::::::::::::|");
    System.out.println("|&&&&::                                  ::  |");
    System.out.println("|&&&&::                                  ::::|");
    System.out.println("|&&&&::                                  ::((|");
    System.out.println("+--------------------------------------------+");
  }
}
