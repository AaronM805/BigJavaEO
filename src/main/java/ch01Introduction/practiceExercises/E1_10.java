package ch01Introduction.practiceExercises;

/**
 * Write a program that prints a house that looks exactly like the following:
 *
 *    +
 *   + +
 *  +   +
 * +-----+
 * | .-. |
 * | | | |
 * +-+-+-+
 *
 * Difficulty: medium
 */
public class E1_10 {
  public static void printHouse() {
    System.out.println("   +");
    System.out.println("  + +");
    System.out.println(" +   +");
    System.out.println("+-----+");
    System.out.println("| .-. |");
    System.out.println("| | | |");
    System.out.println("+-+-+-+");
  }
}
